package day03_EscapeSequences;


/* Escape sequences must be given with double quote

we use \n to print new lines

all java escape sequences are in backward slash\\\


\t tab button, paragraph space

\\ Causes a backslash to be printed


\" causes a double quote to be printed


 */



public class EscapeSequence {


    public static void main(String[] args) {


        System.out.println("Java\nPython\nC#");

        System.out.println(" Hello Cydeo \nHow are you all today? \nIt's nice to see you all! \nWhat class do we have next week?");


        System.out.println("-----------------------------------------");

        System.out.println("\tJava is Cool a Programming Language");

        System.out.println("/ \\");


        System.out.println("My favorite to Show is G\"ame of Thrones\"");

        System.out.println("He said\"My favorite book is Java full edition\"");


        // in order to print double quote we need to use double quote





    }



}

