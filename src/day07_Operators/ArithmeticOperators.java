package day07_Operators;

public class ArithmeticOperators {


    public static void main(String[] args) {
        
        
        int num1=10;
        
        int num2=3;


        int division=num1/num2;  // Division variable gives me the result of num1/num2
        int remainder=num1%num2; // Variable remainder gives me the value of remainder


        System.out.println("division = " + division);
        System.out.println("remainder = " + remainder);


        System.out.println(num1+" divided by "+num2+" is equal to "+division+" with a remainder of "+remainder);


        System.out.println(25%4);

        System.out.println(25%5);

        
    }
    
    
}


